//=============================================================================
// Setup
//=============================================================================
const config = require('config');
const express = require('express');
const app = express();
app.use(express.urlencoded({ extended: true }));

//=============================================================================
// Routes
//=============================================================================
app.use('/api', require('./api'));

//=============================================================================
// Listen for requests
//=============================================================================
app.listen(config.get('port'), () => {
  console.log('#==========================================================');
  console.log(`# API started on port ${config.get('port')}`);
  console.log('#==========================================================');
});
